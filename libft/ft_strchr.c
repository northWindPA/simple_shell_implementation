#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*ns;

	ns = (char *)s;
	while (*ns != c)
	{
		if (*ns == '\0')
			return (NULL);
		ns++;
	}
	return (ns);
}
